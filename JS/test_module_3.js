function countProps(object) {
  let propCount = 0;
  // Change code below this line
for(key in object){
  if(object.hasOwnProperty(key)){
    propCount += 1;
  }
}
  // Change code above this line
  return propCount;
};
console.log(countProps({ name: "Mango", age: 2 }));
